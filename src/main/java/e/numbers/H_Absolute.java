package e.numbers;

public class H_Absolute {
    public static void main(String[] args) {
        // returns the absolute value of an int value
        Integer x = -15;
        float y = -10;
        double z = -50;
        System.out.println(Math.abs(x));
        System.out.println(Math.abs(y));
        System.out.println(Math.abs(z));
    }
}
