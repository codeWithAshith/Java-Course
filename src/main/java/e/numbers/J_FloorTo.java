package e.numbers;

public class J_FloorTo {
    public static void main(String[] args) {
        // returns the double value that is less than or equal to the argument
        // and is equal to the nearest mathematical integer
        double d = -40.989;
        float f = -30.9f;
        System.out.println(Math.floor(d));
        System.out.println(Math.floor(f));
    }
}
