package e.numbers;

public class K_Rint {
    public static void main(String[] args) {
        // is used to round of the floating-point argument to an integer value
        double x = 70.550;
        double y = 70.480;
        System.out.println(Math.rint(x));
        System.out.println(Math.rint(y));
    }
}
