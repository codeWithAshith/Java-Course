package d.operators;

public class C_Logical {
    public static void main(String[] args) {

        // Logical Operators
        boolean a = true;
        boolean b = false;
        System.out.println("a && b = " + (a && b));
        System.out.println("a || b = " + (a || b));
        System.out.println("!(a && b) = " + !(a && b));


    }
}
