package d.operators;

public class D_Conditional {
    public static void main(String[] args) {
        // Conditional Operators
        int a, b;
        a = 20;

        //variable x = (expression) ? value if true : value if false System.out.println( "Value of b is : " + b );
        b = (a == 1) ? 30 : 70;
        //variable x = (expression) ? value if true : value if false
        b = (a == 40) ? 20 : 50;
        System.out.println("Value of b is : " + b);

    }
}
